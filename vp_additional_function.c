#include "ft_printf.h"

void	biggest_modifier(t_eb *k)
{
	if (k->z == 1)
	{
		k->h = 0;
		k->hh = 0;
		k->l = 0;
		k->ll = 0;
		k->j = 0;
	}
	else if (k->j == 1)
	{
		k->h = 0;
		k->hh = 0;
		k->l = 0;
		k->ll = 0;
	}
	else if (k->ll == 1)
	{
		k->h = 0;
		k->hh = 0;
		k->l = 0;
	}
	if ((k->l == 1) && !(k->h = 0))
		k->hh = 0;
	k->h == 1 ? k->hh = 0 : 0;
}

